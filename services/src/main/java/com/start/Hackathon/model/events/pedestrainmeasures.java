package com.start.Hackathon.model.events;

public class pedestrainmeasures {

	Integer counter_direction;
	Float counter_direction_speed;
	Integer counter_direction_pedestrianCount;
	Integer direction;
	Float speed;
	Integer pedestrianCount;

	public Integer getCounter_direction() {
		return counter_direction;
	}

	public void setCounter_direction(Integer counter_direction) {
		this.counter_direction = counter_direction;
	}

	public Float getCounter_direction_speed() {
		return counter_direction_speed;
	}

	public void setCounter_direction_speed(Float counter_direction_speed) {
		this.counter_direction_speed = counter_direction_speed;
	}

	public Integer getCounter_direction_pedestrianCount() {
		return counter_direction_pedestrianCount;
	}

	public void setCounter_direction_pedestrianCount(Integer counter_direction_pedestrianCount) {
		this.counter_direction_pedestrianCount = counter_direction_pedestrianCount;
	}

	public Integer getDirection() {
		return direction;
	}

	public void setDirection(Integer direction) {
		this.direction = direction;
	}

	public Float getSpeed() {
		return speed;
	}

	public void setSpeed(Float speed) {
		this.speed = speed;
	}

	public Integer getPedestrianCount() {
		return pedestrianCount;
	}

	public void setPedestrianCount(Integer pedestrianCount) {
		this.pedestrianCount = pedestrianCount;
	}

}
